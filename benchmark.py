def run_benchmark():
    print("=" * 60)
    print("  교통사고 판례 RAG 벤치마크 (150건)")
    print("=" * 60)

    t0 = time.time()
    rag = PrecedentRAG()
    load_time = time.time() - t0
    print(f"\n모델 로딩: {load_time:.1f}초\n")
    print("-" * 60)

    total = len(TEST_CASES)
    hits = []
    misses = []
    scores = []
    latencies = []

    for i, (query, expected) in enumerate(TEST_CASES):
        t_start = time.time()
        results = rag.search(query, top_k=1)
        latency = time.time() - t_start
        latencies.append(latency)

        if not results:
            misses.append((i, query, "결과 없음"))
            scores.append(0.0)
            continue

        r = results[0]
        text = f"{r['사건명']} {r['판시사항']} {r['판결요지']}"
        matched = [kw for kw in expected if kw in text]
        score = r["score"]
        scores.append(score)

        if matched:
            hits.append(i)
        else:
            misses.append((i, query, r["사건번호"]))

    # ── 결과 출력 ──
    print("\n" + "=" * 60)
    print("  벤치마크 결과")
    print("=" * 60)

    hit_count = len(hits)
    print(f"\n전체 정확도: {hit_count}/{total} ({hit_count/total*100:.1f}%)")

    print(f"\n카테고리별 정확도:")
    for cat_name, idx_range in CATEGORIES.items():
        cat_hits = sum(1 for i in idx_range if i in hits)
        cat_total = len(idx_range)
        pct = cat_hits/cat_total*100
        bar = "█" * cat_hits + "░" * (cat_total - cat_hits)
        print(f"  {cat_name:20s} {bar} {cat_hits}/{cat_total} ({pct:.0f}%)")

    valid_scores = [s for s in scores if s > 0]
    print(f"\n유사도 분포:")
    print(f"  평균: {sum(valid_scores)/len(valid_scores):.4f}")
    print(f"  최소: {min(valid_scores):.4f}")
    print(f"  최대: {max(valid_scores):.4f}")

    buckets = {"0.9+": 0, "0.8~0.9": 0, "0.7~0.8": 0, "<0.7": 0}
    for s in valid_scores:
        if s >= 0.9: buckets["0.9+"] += 1
        elif s >= 0.8: buckets["0.8~0.9"] += 1
        elif s >= 0.7: buckets["0.7~0.8"] += 1
        else: buckets["<0.7"] += 1
    for label, count in buckets.items():
        bar = "█" * count
        print(f"  {label:10s} {bar} {count}건")

    print(f"\n검색 속도 (쿼리당):")
    avg_lat = sum(latencies)/len(latencies)
    sorted_lat = sorted(latencies)
    print(f"  평균: {avg_lat:.2f}초")
    print(f"  최소: {sorted_lat[0]:.2f}초")
    print(f"  최대: {sorted_lat[-1]:.2f}초")
    print(f"  p50:  {sorted_lat[len(sorted_lat)//2]:.2f}초")
    print(f"  p95:  {sorted_lat[int(len(sorted_lat)*0.95)]:.2f}초")
    print(f"  총 소요: {sum(latencies):.1f}초")

    print(f"\nDB 정보:")
    print(f"  벡터 수: 1701건 (dense+sparse, 멀티포인트 청킹)")
    print(f"  검색: 하이브리드(dense+sparse RRF) → ColBERT+CE 앙상블 리랭킹")

    if misses:
        print(f"\n미스 목록 ({len(misses)}건):")
        for idx, q, info in misses:
            cat = ""
            for cname, crange in CATEGORIES.items():
                if idx in crange:
                    cat = cname
                    break
            print(f"  [{idx+1:3d}] [{cat}] {q}")
            print(f"        → {info}")

    print("\n" + "=" * 60)


if __name__ == "__main__":
    run_benchmark()
